package com.exercises.ex01;

public class SwithCaseStatement {

	public SwithCaseStatement() {	}

	public static void main(String[] args) {
		char c = '5';
		
		switch (c) {
		default:
			System.out.println("default"); 
			break;
		case '1': 
			System.out.println("1"); 
			break;
		case '2': 
			System.out.println("1");
			break;
		case 'a': 
			System.out.println("1");
			break;
		case 'b': 
			System.out.println("1");
			break;
		case 'c': 
			System.out.println("1");
			break;		
		}
	}
	
}
